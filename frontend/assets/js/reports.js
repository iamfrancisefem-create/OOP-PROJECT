import { ReportAPI, ProjectAPI, TaskAPI } from './api.js';
import { DateHelper, toast } from './utils.js';
import Chart from 'chart.js/auto';

export async function loadProjectOptions(selectId) {
  try {
    const res = await ProjectAPI.getAll(0, 100);
    const projects = res.data.data?.content || res.data.data || [];
    const sel = document.getElementById(selectId);
    if (sel) sel.innerHTML = '<option value="">Select a project...</option>' + projects.map(p => `<option value="${p.id}">${p.title}</option>`).join('');
  } catch (e) {}
}

export async function generateReport(projectIdEl, typeEl, btnEl, loadCb) {
  const projectId = document.getElementById(projectIdEl).value;
  const reportType = document.getElementById(typeEl).value;
  if (!projectId) { toast('Please select a project', 'warning'); return; }
  const btn = document.getElementById(btnEl);
  btn.disabled = true; btn.innerHTML = '<span class="spinner"></span>';
  try {
    await ReportAPI.generate(projectId, reportType);
    toast('Report generated!', 'success');
    if (loadCb) loadCb();
  } catch (e) {
    toast('Failed to generate report', 'error');
  } finally {
    btn.disabled = false; btn.innerHTML = '<i class="fa-solid fa-file-export"></i> Generate';
  }
}

export async function loadReports(containerId) {
  const container = document.getElementById(containerId);
  try {
      const res = await ReportAPI.getByProject(projectId || 'all', 0);
    const reports = res.data.data?.content || res.data.data || [];
    if (!reports.length) {
      container.innerHTML = `<div class="empty-state"><div class="empty-icon"><i class="fa-solid fa-file"></i></div><h4>No reports yet</h4><p>Generate a report to see it here.</p></div>`;
      return;
    }
    container.innerHTML = reports.map(r => `
      <div class="report-card">
        <div class="report-icon" style="background:var(--primary-light);color:var(--primary)"><i class="fa-solid fa-file-lines"></i></div>
        <div class="report-info">
          <h4>${r.reportType || 'Report'}</h4>
          <p>Generated by ${r.generatedByName || 'System'}</p>
          <div class="report-meta">
            <span><i class="fa-regular fa-calendar"></i> ${DateHelper.format(r.createdAt)}</span>
            <span><i class="fa-solid fa-download"></i> <a href="${ReportAPI.download(r.id)}" style="color:var(--primary)">Download</a></span>
          </div>
        </div>
      </div>
    `).join('');
  } catch (e) {}
}

export async function loadTaskChart(canvasId) {
  try {
    const res = await TaskAPI.getAll(0, 100);
    const tasks = res.data.data?.content || res.data.data || [];
    const counts = { TODO: 0, IN_PROGRESS: 0, TESTING: 0, DONE: 0 };
    tasks.forEach(t => { if (counts[t.status] !== undefined) counts[t.status]++; });
    const ctx = document.getElementById(canvasId)?.getContext('2d');
    if (!ctx) return;
    new Chart(ctx, {
      type: 'doughnut',
      data: {
        labels: ['To Do', 'In Progress', 'Testing', 'Done'],
        datasets: [{ data: [counts.TODO, counts.IN_PROGRESS, counts.TESTING, counts.DONE], backgroundColor: ['#94A3B8', '#2563EB', '#F59E0B', '#10B981'], borderWidth: 0 }],
      },
      options: { responsive: true, plugins: { legend: { position: 'bottom' } } },
    });
  } catch (e) {}
}
